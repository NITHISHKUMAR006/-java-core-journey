public class Variables {
    public static void main(String[] args) {
        byte age = 20;
        short year = 2024;
        int population = 1000000;
        long worldPop = 8000000000L;
        float pi = 3.14f;
        double precisePi = 3.14159265358979;
        char grade = 'A';
        boolean isStudent = true;

        System.out.println("=== Variable Demo ===");
        System.out.println("Age: " + age);
        System.out.println("Year: " + year);
        System.out.println("Population: " + population);
        System.out.println("World Population: " + worldPop);
        System.out.println("Pi (float): " + pi);
        System.out.println("Pi (double): " + precisePi);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + isStudent);
    }
}
